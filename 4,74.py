a2 = int(input("a2 (десятки первого): "))
a1 = int(input("a1 (единицы первого): "))
b2 = int(input("b2 (десятки второго): "))
b1 = int(input("b1 (единицы второго): "))
A = a2 * 10 + a1
B = b2 * 10 + b1
diff = A - B
d2 = diff // 10  
d1 = diff % 10   
print(f"Цифры разности: десятки = {d2}, единицы = {d1}")
