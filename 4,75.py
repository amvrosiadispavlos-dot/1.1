a3 = int(input("a3 (сотни трёхзначного): "))
a2 = int(input("a2 (десятки трёхзначного): "))
a1 = int(input("a1 (единицы трёхзначного): "))
b2 = int(input("b2 (десятки двузначного): "))
b1 = int(input("b1 (единицы двузначного): "))
A = a3 * 100 + a2 * 10 + a1
B = b2 * 10 + b1
total = A + B
t3 = total // 100       
t2 = (total // 10) % 10  
t1 = total % 10    
print(f"Цифры суммы: сотни = {t3}, десятки = {t2}, единицы = {t1}")
