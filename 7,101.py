print("Введите последовательность вещественных чисел, оканчивающуюся 10000:")
seq = []
while True:
    num = float(input())
    if num == 10000:
        break
    seq.append(num)
for i in range(len(seq) - 1):
    if seq[i] >= seq[i+1]:
        print(f"Последовательность не упорядочена по возрастанию.")
        print(f"Первое число, нарушающее порядок: позиция {i+2} (число {seq[i+1]})")
        break
else:
    print("Последовательность упорядочена по возрастанию.")
